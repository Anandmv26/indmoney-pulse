import os
import base64
from email.message import EmailMessage
from dotenv import load_dotenv
from googleapiclient.discovery import build

from src.mcp.auth import get_google_credentials


load_dotenv()


def format_body(payload: dict) -> str:
    """
    Format payload into the email body text.
    """
    pulse = payload["weekly_pulse"]
    top_themes = " | ".join(pulse["top_themes"])
    note = pulse["note"]
    actions = "\n".join(f"{i+1}. {a}" for i, a in enumerate(pulse["actions"]))
    quotes = "\n".join(f"- \"{q['text']}\"" for q in pulse["quotes"])
    
    fee_scenario = payload["fee_scenario"]
    bullets = "\n".join(f"• {b}" for b in payload["explanation_bullets"])
    sources = "\n".join(f"- {s['label']}: {s['url']}" for s in payload["source_links"])
    
    return f"""Hi team,

== WEEKLY PRODUCT PULSE — {payload["date"]} ==

{note}

Top themes: {top_themes}

Action ideas:
{actions}

User quotes:
{quotes}


== FEE EXPLAINER — {fee_scenario} ==

{bullets}

Official sources:
{sources}

Last checked: {payload["last_checked"]}

---
Generated by INDmoney AI Workflow. This is a draft — review before sending."""


