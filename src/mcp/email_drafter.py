def create_draft(payload: dict) -> bool:
    """
    Create a Gmail draft using Google official APIs directly. 
    Matches HC-8 (create_draft only, no auto-send).
    Returns True on success, False on failure.
    """
    creds = get_google_credentials()
    recipient = os.getenv("DRAFT_RECIPIENT")

    if not recipient:
        print("Gmail draft failed: Missing DRAFT_RECIPIENT in environment.")
        return False
    if not creds:
        print("Gmail draft failed: Invalid or missing GOOGLE_CREDENTIALS_BASE64.")
        return False

    subject = f"Weekly Pulse + Fee Explainer — {payload['date']}"
    text_body = format_body(payload)
    html_body = format_html_body(payload)

    # Compile proper MIME email required for drafting
    message = EmailMessage()
    message["To"] = recipient
    message["Subject"] = subject
    message.set_content(text_body)
    message.add_alternative(html_body, subtype='html')

    # Base64url encoded representation of the raw MIME standard
    encoded_message = base64.urlsafe_b64encode(message.as_bytes()).decode()

    create_message = {
        "message": {
            "raw": encoded_message
        }
    }

    try:
        # Build Standard Gmail V1 endpoint
        service = build("gmail", "v1", credentials=creds, cache_discovery=False)
        
        # We explicitly enforce drafts().create() instead of messages.send! (HC-8)
        service.users().drafts().create(
            userId="me", 
            body=create_message
        ).execute()

        print("Gmail API draft created successfully. Check your Drafts folder.")
        return True
    
    except Exception as e:
        print(f"Gmail drafting API failed: {e}")
        return False
